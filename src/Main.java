public class Main {
    public static void main(String[] args) {
        int switchValue = 1;

        switch(switchValue) {
            case 1:
                System.out.println("The value is 1");
                break;
            case 2:
                System.out.println("The value is 2");
                break;
            default:
                System.out.println("The value is not 1 or 2");
        }
    }
}